# python len()  methods

# creating a string first


string1 = "Shoutoutloud"

print(f"The length of the string {string1} is ")

print(len(string1))

# finding the  length of tuple & list by using len

# creating a tuple

t1 = (2, 3, 4, 5)

print(f"The length of the tuple {t1} is ")

print(len(t1))

l1 = [2, 3, 4, 5]

print(f"The length of the list {l1} is ")

print(len(l1))


# finding the length of the dictionary by using len()

# creating a dic

dic = {"a": 1, "b": 2}

print(f"The length of the dictionary {dic} is ")

print(len(dic))
