# extanding new elements by using .extend()  methods


# creating a list first

l1 = [1, 2, 3, 4, 5]

print("Before extending the list is ")

print(l1)

# extending the list by using .extend()  method

l1.extend([7, "Use Geeks for programming "])

print("After extending the list becomes ")

print(l1)


# reversing a list by using  .reverse()  method

l1.reverse()

print("After reversing the list becomes ")

print(l1)


# removing the elements by using  .remove()  function

l1.remove(7)

print("After removing the list becomes ")

print(l1)
