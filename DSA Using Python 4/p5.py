# type casting of objects in python into sets

# creating a list first

l = [1, 2, 3, 4, 5, 6]

# making it to set

s = set(l)

print("The list as a st is ", s)

# making a string and changing it to set

mystr = "SubhamandShreyoshi"

s1 = set(mystr)

