# union function in set

# creating two sets

a = {1, 2, 3, 5, 6, 7}

b = {4, 8, 9, 10, 11}

print(f"The sunion of {a} and {b}  is ")

print(a.union(b))


# union in three sets

a1 = {1, 2, 3, 4}

b1 = {3, 4, 5, 6}

c1 = {5, 6, 7, 8}

print("The union of three sets are ")

print(a1.union(b1, c1))
