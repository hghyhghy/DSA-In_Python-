# intersection fnction in set

# creating two sets

a = {1, 2, 3, 4}

b = {2, 3, 4}

print(f"The intersection of the set {a} and {b}  is ")

print(a.intersection(b))


# intersection between three  sets

s1 = {1, 2, 3, 4}

s2 = {2, 3, 4, 5}

s3 = {3, 4, 5, 6}

print("The intersection of three  sets are ")

print(s1.intersection(s2, s3))


# python set intersection

# by using & operator

s0 = {1, 2, 3, 4}

s4 = {2, 3, 4, 5}


print(f"The intersection between {s0} and {s4} is ")

print(s0 & s4)
